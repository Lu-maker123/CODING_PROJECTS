    private void depo_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depo_buttonActionPerformed
        // process of adding money to balance when button is pressed and
        // retreives amount from textfield
        int amount = Integer.parseInt(depo_amount.getText());
        if (amount > 0){
            balance += amount;
            balance_amount.setText(String.valueOf(balance));
        }
    }//GEN-LAST:event_depo_buttonActionPerformed

