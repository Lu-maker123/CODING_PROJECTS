    private void with_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_with_buttonActionPerformed
        // process of taking money from balance when button is pressed and
        // retreives amount from the textfield
        int amount = Integer.parseInt(with_amount.getText());
        if (amount <= balance){
            balance -= amount;
            balance_amount.setText(String.valueOf(balance));
        }
    }//GEN-LAST:event_with_buttonActionPerformed

